package com.hw;

public class DoubleFunctions {
    double doubleValue1 = 4.17;
    double doubleValue2 = 7.346;

    public void doubleAddition() {
        double result = doubleValue1 + doubleValue2;
        System.out.println("doubleAddition is " + result);
    }

    public void doubleSubtraction() {
        double result = doubleValue1 - doubleValue2;
        System.out.println("doubleSubtraction is " + result);
    }

    public void doubleMultiplication() {
        double result = doubleValue1 * doubleValue2;
        System.out.println("doubleMultiplication is " + result);
    }

    public void doubleDivision() {
        double result = doubleValue1 / doubleValue2;
        System.out.println("doubleDivision is " + result);
    }

    public void doubleModuloDivision() {
        double result = doubleValue1 % doubleValue2;
        System.out.println("doubleModuloDivision is " + result);
    }
}
