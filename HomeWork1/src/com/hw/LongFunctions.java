package com.hw;

public class LongFunctions {
    long longValue1 = 4L;
    long longValue2 = 17L;

    public void longAddition() {
        long result = longValue1 + longValue2;
        System.out.println("longAddition is " + result);
    }

    public void longSubtraction() {
        long result = longValue1 - longValue2;
        System.out.println("longSubtraction is " + result);
    }

    public void longMultiplication() {
        long result = longValue1 * longValue2;
        System.out.println("longMultiplication is " + result);
    }

    public void longDivision() {
        long result = longValue1 / longValue2;
        System.out.println("longDivision is " + result);
    }

    public void longModuloDivision() {
        long result = longValue1 % longValue2;
        System.out.println("longModuloDivision is " + result);
    }
}
