package com.hw;

public class FloatFunctions {
    float floatValue1 = 22F;
    float floatValue2 = 7F;

    public void floatAddition() {
        float result = floatValue1 + floatValue2;
        System.out.println("floatAddition is " + result);
    }

    public void floatSubtraction() {
        float result = floatValue1 - floatValue2;
        System.out.println("floatSubtraction is " + result);
    }

    public void floatMultiplication() {
        float result = floatValue1 * floatValue2;
        System.out.println("floatMultiplication is " + result);
    }

    public void floatDivision() {
        float result = floatValue1 / floatValue2;
        System.out.println("floatDivision is " + result);
    }

    public void floatModuloDivision() {
        float result = floatValue1 % floatValue2;
        System.out.println("floatModuloDivision is " + result);
    }
}
